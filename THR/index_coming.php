<p>ජාතික අධ්‍යාපන කළමනාකරණ තොරතුරු පද්ධතිය නව සර්වර් පරිසරය වෙත සංක්‍රමණය කිරීමේ කටයුතු මේ වනවිට සාර්ථකව අවසන් කර ඇත. ඒ අනුව තොරතුරු පද්ධතිය වෙතට ප්‍රවේශ වී පසුගිය කාලය තුල ඇතුලත් කරන ලද තොරතුරු වල අඩුපාඩු සකස් කිරීම සියළුම කලාප කර්යාල මගින් සිදු කලයුතු වන අතර ඉන් පසුව ඒ ඒ කට්ටල වල සිදුවන සංශෝධනයන් පද්ධතිය වෙත ඇතුලත් කර අඛණ්ඩව තොරතුරු පද්ධතිය පවත්වාගෙන යා යුතුය. එසේ තොරතුරු පද්ධතිය වෙතට ප්‍රවේශ වී අදාළ අඩුපාඩු සකස් කිරීම සඳහා එක් එක් කලාපය විසින් මෙතෙක් ඇතුලත් කරන ලද දත්ත පිලිබඳ වාර්තාව ඒ ඒ කලාප කාර්යාලය වෙත එවීමෙන් පසුව එම කටයුතු සිදුකිරීම සඳහා නිලවශයෙන් දැනුම්දෙනු ඇත. </p>

<p>NEMIS System புதிய server இற்கு இடமாற்றும் செயற்பாடு வெற்றிகரமாக நிறைவு செய்யப்பட்டுள்ளது. அண்மைக்காலத்தில் திருத்தப்பட்ட தரவுகளைப்  பெறுவதற்காக NEMIS இற்கான அணுகலை (Access) வலய ரீதியாக வழங்க நடவடிக்கை எடுக்கப்பட்டுள்ளன. அந்தந்த வலயங்களுக்கு தரவுத் தொகுப்பு அனுப்பப்பட்டவுடன். குறித்த வலயங்களுக்கு உத்தியோகப்பூர்வமாக அறிவிக்கப்படும்.</p>  

<p>The NEMIS system migration to the new server environment has been successfully completed. Actions have been taken to provide access to the NEMIS gradually to the zones in order to attend the corrections of data that were entered during the recent past and also to attend to the updates of each personal file as and when required. This will be officially informed to each zone once the respective data-set has been sent to the zone. </p>

